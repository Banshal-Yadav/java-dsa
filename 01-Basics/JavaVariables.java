// variables in java
public class JavaVariables {
    public static void main(String args[]) {
        // integer variable
        int a = 6;
        System.out.print(a+ "\n");

        // string variable
        String name = "Banshal Yadav";
        System.out.print(name +"\n");

        // re-defining the variable 
        a = 50;
        System.out.print(a);
    }
}
